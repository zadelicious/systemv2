    private void LOGINActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_LOGINActionPerformed
       if (isLocked) {
            JOptionPane.showMessageDialog(this, "Account is temporarily locked. Please wait.");
            return;
        }

        String el = txtemail.getText();
        String initial = new String(txtpassword.getPassword()); // Correctly convert char[] to String

        try {
            String hashedPassword = PasswordHashing.hashPassword(initial);

            Class.forName("com.mysql.cj.jdbc.Driver");
            con = DriverManager.getConnection("jdbc:mysql://localhost:3306/inventorymanagement", "root", "");
            ps = con.prepareStatement("SELECT userrole, name FROM inventorymanagementsystem WHERE username = ? AND password = ?");
            ps.setString(1, el);
            ps.setString(2, hashedPassword);
            rs = ps.executeQuery();

            temp = 0;
            while (rs.next()) {
                temp = 1;
                String userName = rs.getString("name");
                String userRole = rs.getString("userrole");
                setVisible(false);
                new Home(userRole, userName).setVisible(true);
            }
            if (temp == 0) {
                handleFailedLogin(); // Call the updated method here
            }
        } catch (ClassNotFoundException | SQLException e) {
            JOptionPane.showMessageDialog(this, e);
        }
    }//GEN-LAST:event_LOGINActionPerformed

