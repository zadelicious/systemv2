    private static final int MAX_LOGIN_ATTEMPTS = 5;
    private int loginAttempts = 0;
    private boolean isLocked = false;
    private Timer lockTimer;

    private void handleFailedLogin() {
        loginAttempts++;
        if (loginAttempts >= MAX_LOGIN_ATTEMPTS) {
            isLocked = true;
            LOGIN.setEnabled(false);

            // Start the lock timer
            lockTimer = new Timer(30000, (e) -> {
                isLocked = false;
                loginAttempts = 0;
                LOGIN.setEnabled(true);

                // Stop the timer after execution
                ((Timer) e.getSource()).stop();
            });
            lockTimer.setRepeats(false); // Ensure the timer executes only once
            lockTimer.start();

            JOptionPane.showMessageDialog(this, "Account temporarily locked. Please wait 30 seconds.");
        } else {
            JOptionPane.showMessageDialog(this, "Incorrect credentials. Attempts remaining: "
                    + (MAX_LOGIN_ATTEMPTS - loginAttempts));
        }
    }

